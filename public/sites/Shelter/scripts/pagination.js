//выводим список страниц
function createPage(paginator, actualIndex) {
  let page = "";
  if (innerWidth > 767) {
    for (var i = 0; i < cnt_page; i++) {
      page += "<div class=\"simple-btn simple-btn-circle\" data-page=" + (i + 1) + "  id=\"" + (i + 1) + "\">" + (i + 1) + "</div>";
    }
  } else {
    if (Number(actualIndex) === 0) {
      for (var i = Number(actualIndex); i < Number(actualIndex) + 3; i++) {
        page += "<div class=\"simple-btn simple-btn-circle\" data-page=" + (i + 1) + "  id=\"" + (i + 1) + "\">" + (i + 1) + "</div>";
      }
      page += "<span>...</span>"
      page += "<div class=\"simple-btn simple-btn-circle\" data-page=" + cnt_page + "  id=\"" + cnt_page + "\">" + cnt_page + "</div>";
    } else if (Number(actualIndex) > 0 && Number(actualIndex) < Number(cnt_page) - 2) {
      for (var i = Number(actualIndex) - 1; i < Number(actualIndex) + 2; i++) {
        page += "<div class=\"simple-btn simple-btn-circle\" data-page=" + (i + 1) + "  id=\"" + (i + 1) + "\">" + (i + 1) + "</div>";
      }
      page += "<span>...</span>"
      page += "<div class=\"simple-btn simple-btn-circle\" data-page=" + cnt_page + "  id=\"" + cnt_page + "\">" + cnt_page + "</div>";
    } else if (Number(actualIndex) >= cnt_page - 2 || Number(actualIndex) < Number(cnt_page)) {
      page += "<div class=\"simple-btn simple-btn-circle\" data-page=" + 1 + "  id=\"" + 1 + "\">" + 1 + "</div>";
      page += "<span>...</span>"
      for (var i = Number(cnt_page) - 3; i < Number(cnt_page); i++) {
        page += "<div class=\"simple-btn simple-btn-circle\" data-page=" + (i + 1) + "  id=\"" + (i + 1) + "\">" + (i + 1) + "</div>";
      }
    }
  }
  paginator.innerHTML = page;
  numBtn = document.querySelectorAll('div[data-page]');
  clkBtn(numBtn)
}

//создаём псевдонабор животных 
function generateArray() {
  let newArr = [];
  for (let i = 0; i < cnt_page; i++) {
    let subArr = [];
    while (subArr.length < cnt) {
      const randomNumber = Math.floor(Math.random() * 8);
      if (!subArr.includes(randomNumber)) {
        subArr.push(randomNumber);
      }
    }
    newArr.push(subArr)
  }
  return newArr
}


//выводим животных на страницу
function createCard(arr) {
  for (let j = 0; j < cnt; j++) {
    let card = document.createElement('div')
    card.classList.add("pets-carusel__card");
    card.setAttribute('data-name', PETS[arr[j]].name)
    card.setAttribute('data-id', arr[j])
    let cardI = document.createElement('img');
    cardI.setAttribute('src', PETS[arr[j]].img);
    cardI.setAttribute('alt', 'pet');
    let cardP = document.createElement('p');
    cardP.innerHTML = PETS[arr[j]].name;
    let cardB = document.createElement('button');
    cardB.innerHTML = 'Learn more';
    cardB.classList.add('simple-btn');
    card.append(cardI);
    card.append(cardP);
    card.append(cardB);
    crdWrp.append(card)
    actualIndex = arrPages.indexOf(arr)
  };
}

